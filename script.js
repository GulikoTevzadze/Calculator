/*рЃерЃћрЃЋрЃЦрЃЏрЃћрЃюрЃўрЃЌ рЃЏрЃљрЃАрЃўрЃЋрЃў naruto */
const naruto = ["naruto", "sasuke", "sakura", "itachi", "kakashi"];
/* рЃерЃћрЃЋрЃЦрЃЏрЃћрЃюрЃўрЃЌ рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃарЃљрЃюрЃЊрЃЮрЃЏрЃБрЃџрЃљрЃЊ рЃљрЃарЃЕрЃЋрЃћрЃЋрЃА рЃЏрЃљрЃАрЃўрЃЋрЃўрЃЊрЃљрЃю рЃАрЃўрЃбрЃДрЃЋрЃљрЃА рЃЏрЃўрЃАрЃў рЃўрЃюрЃЊрЃћрЃ«рЃАрЃўрЃА рЃЏрЃўрЃ«рЃћрЃЊрЃЋрЃўрЃЌ рЃЊрЃљ рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃЏрЃўрЃА рЃўрЃюрЃЊрЃћрЃЦрЃА */
function randomWord() {
  const randomIndex = Math.floor(Math.random() * naruto.length);
  return naruto[randomIndex];
}

function game() {
  /*рЃўрЃФрЃљрЃ«рЃћрЃЉрЃљ рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃњрЃљрЃЏрЃЊрЃЮрЃЏрЃБрЃџ рЃАрЃўрЃбрЃДрЃЋрЃљрЃА рЃЏрЃљрЃАрЃўрЃЋрЃўрЃЊрЃљрЃю рЃЊрЃљ рЃўрЃюрЃљрЃ«рЃћрЃЉрЃљ рЃЕрЃЋрЃћрЃюрЃА рЃЏрЃўрЃћрЃа рЃерЃћрЃЦрЃЏрЃюрЃўрЃџ рЃфрЃЋрЃџрЃљрЃЊрЃерЃў secretWord, рЃЋрЃЦрЃЏрЃюрЃўрЃЌ рЃЏрЃљрЃАрЃўрЃЋрЃА Array.from-рЃўрЃЌ, рЃДрЃЋрЃћрЃџрЃљ рЃЏрЃљрЃАрЃўрЃЋрЃўрЃА рЃўрЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃА рЃЋрЃДрЃЮрЃцрЃЌ "_",рЃерЃћрЃЋрЃЦрЃЏрЃюрЃљрЃЌ рЃфрЃЋрЃџрЃљрЃЊрЃў let incorrectattempt рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃЏрЃЮрЃЏрЃљрЃЋрЃљрЃџрЃе рЃЊрЃљрЃўрЃЌрЃЋрЃџрЃўрЃА рЃерЃћрЃфрЃЊрЃЮрЃЏрЃћрЃЉрЃА, рЃЌрЃљрЃЋрЃЊрЃљрЃърЃўрЃарЃЋрЃћрЃџрЃљрЃЊ = 0; */
  const secretWord = randomWord();
  let guessedWord = Array.from({ length: secretWord.length }, () => "_");
  let incorrectattempt = 0;
/**рЃерЃћрЃЋрЃЦрЃЏрЃюрЃљрЃЌ рЃфрЃўрЃЎрЃџрЃў while, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃњрЃарЃФрЃћрЃџрЃЊрЃћрЃЉрЃљ рЃАрЃљрЃюрЃљрЃЏ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃў рЃљрЃа рЃњрЃљрЃЏрЃЮрЃўрЃфрЃюрЃЮрЃЉрЃА рЃАрЃўрЃбрЃДрЃЋрЃљрЃА, рЃарЃЮрЃЏрЃарЃџрЃўрЃф рЃЊрЃљрЃљрЃњрЃћрЃюрЃћрЃарЃўрЃарЃљ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљрЃЏ. join("") рЃЏрЃљрЃАрЃўрЃЋрЃўрЃА рЃЏрЃћрЃЌрЃЮрЃЊрЃўрЃљ рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃљрЃарЃЌрЃўрЃљрЃюрЃћрЃЉрЃА рЃЏрЃљрЃАрЃўрЃЋрЃўрЃА рЃДрЃЋрЃћрЃџрЃљ рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃА string-рЃерЃў */
  while (guessedWord.join("") !== secretWord) {
    const user = prompt(
      `рЃАрЃўрЃбрЃДрЃЋрЃљ: ${guessedWord.join(" ")}
    рЃљрЃарЃљрЃАрЃгрЃЮрЃарЃў рЃЏрЃфрЃЊрЃћрЃџрЃЮрЃљ: ${incorrectattempt}
    рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћрЃЌ рЃљрЃАрЃЮ`
    );
    /*рЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃА рЃерЃћрЃДрЃЋрЃљрЃюрЃўрЃџрЃў рЃљрЃАрЃЮ, рЃ«рЃЮрЃЏ рЃљрЃа рЃљрЃарЃўрЃА рЃљрЃю рЃфрЃљрЃарЃўрЃџрЃћрЃџрЃў рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮ, рЃљрЃарЃўрЃА рЃЌрЃБ рЃљрЃарЃљ рЃерЃћрЃДрЃЋрЃљрЃюрЃўрЃџрЃў рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ 1 рЃљрЃАрЃЮ, рЃљрЃарЃўрЃА рЃЌрЃБ рЃљрЃарЃљ, рЃўрЃюрЃњрЃџрЃўрЃАрЃБрЃарЃў рЃљрЃАрЃЮ рЃерЃћрЃДрЃЋрЃљрЃюрЃўрЃџрЃў */
    if (!user || user.length !== 1 || !/[a-zA-Z]/.test(user)) {
      alert("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћрЃб рЃАрЃгрЃЮрЃарЃў рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮ");
      continue;
    }
    /*рЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃА рЃљрЃарЃўрЃА рЃЌрЃБ рЃљрЃарЃљ рЃерЃћрЃДрЃЋрЃљрЃюрЃўрЃџрЃў рЃљрЃАрЃЮ secretWord, рЃЌрЃБ рЃљрЃарЃўрЃА рЃљрЃњрЃарЃФрЃћрЃџрЃћрЃЉрЃЌ рЃерЃћрЃДрЃЋрЃљрЃюрЃљрЃА*/
    if (secretWord.includes(user)) {
      alert("рЃАрЃгрЃЮрЃарЃўрЃљ! рЃњрЃљрЃљрЃњрЃарЃФрЃћрЃџрЃћрЃЌ");
      /**рЃфрЃўрЃЎрЃџрЃў for рЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃА рЃДрЃЋрЃћрЃџрЃљ рЃљрЃАрЃЮрЃА  */
      for (let i = 0; i < secretWord.length; i++) {
        /*рЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃА рЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљ рЃЌрЃБ рЃљрЃарЃљ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃА рЃЏрЃўрЃћрЃа рЃерЃћрЃДрЃЋрЃљрЃюрЃўрЃџрЃў рЃљрЃАрЃЮ рЃарЃљрЃюрЃЊрЃЮрЃЏрЃБрЃџрЃў рЃАрЃўрЃбрЃДрЃЋрЃўрЃА рЃљрЃАрЃЮрЃА, рЃЌрЃБ рЃ«рЃЮ рЃљрЃюрЃљрЃфрЃЋрЃџрЃћрЃЉрЃА _ рЃљрЃАрЃЮрЃЌ */
        if (secretWord[i] === user) {
          guessedWord[i] = user;
        }
      }
    } else {
      /* рЃЌрЃБ рЃљрЃарЃљрЃАрЃгрЃЮрЃарЃўрЃљ рЃерЃћрЃфрЃЊрЃЮрЃЏрЃћрЃЉрЃўрЃА рЃЏрЃфрЃЊрЃћрЃџрЃЮрЃЉрЃљ рЃўрЃќрЃарЃЊрЃћрЃЉрЃљ */
      alert("рЃљрЃарЃљрЃАрЃгрЃЮрЃарЃўрЃљ, рЃАрЃфрЃљрЃЊрЃћрЃЌ рЃЎрЃўрЃЊрЃћрЃЋ рЃћрЃарЃЌрЃ«рЃћрЃџ");
      incorrectattempt++;
    }
  } /*рЃарЃЮрЃЊрЃћрЃАрЃљрЃф рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃў рЃњрЃљрЃЏрЃЮрЃўрЃфрЃюрЃЮрЃЉрЃА рЃАрЃўрЃбрЃДрЃЋрЃљрЃА рЃњрЃљрЃЏрЃЮрЃЊрЃўрЃА рЃерЃћрЃАрЃљрЃЉрЃљрЃЏрЃўрЃАрЃў alert */
  alert(`рЃњрЃўрЃџрЃЮрЃфрЃљрЃЋрЃЌ! рЃЌрЃЦрЃЋрЃћрЃю рЃњрЃљрЃЏрЃЮрЃўрЃфрЃљрЃюрЃўрЃЌ рЃАрЃўрЃбрЃДрЃЋрЃљ: ${secretWord}`);
}

game();
/*рЃерЃћрЃЋрЃЦрЃЏрЃюрЃЌ рЃфрЃЋрЃџрЃљрЃЊрЃћрЃЉрЃў, рЃарЃЮрЃЏрЃџрЃћрЃЉрЃўрЃф рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА рЃЊрЃўрЃљрЃърЃљрЃќрЃЮрЃюрЃА рЃњрЃљрЃюрЃАрЃљрЃќрЃдрЃЋрЃарЃљрЃЋрЃћрЃю */
const minNumber = 1;
const maxNumber = 5;
/* рЃЊрЃљрЃЋрЃљрЃњрЃћрЃюрЃћрЃарЃўрЃарЃЮрЃЌ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃўрЃЌрЃў рЃарЃўрЃфрЃ«рЃЋрЃў рЃЏрЃЮрЃфрЃћрЃЏрЃБрЃџ рЃЊрЃўрЃљрЃърЃљрЃќрЃЮрЃюрЃерЃў*/
const answer =
  Math.floor(Math.random() * (maxNumber - minNumber + 1)) + minNumber;

/* рЃерЃћрЃЋрЃЦрЃЏрЃюрЃљрЃЌ рЃфрЃЋрЃџрЃљрЃЊрЃћрЃЉрЃў рЃфрЃўрЃЎрЃџрЃўрЃА рЃњрЃљрЃАрЃљрЃЎрЃЮрЃюрЃбрЃарЃЮрЃџрЃћрЃЉрЃџрЃљрЃЊ рЃЊрЃљ рЃЏрЃфрЃЊрЃћрЃЉрЃЮрЃЉрЃўрЃА рЃарЃљрЃЮрЃЊрЃћрЃюрЃЮрЃЉрЃўрЃА рЃњрЃљрЃАрЃљрЃЎрЃЮрЃюрЃърЃарЃЮрЃџрЃћрЃЉрЃџрЃљрЃЊ*/
let attempts = 0;
let nextStep = true;

/*рЃЊрЃљрЃЋрЃўрЃгрЃДрЃЮрЃЌ рЃфрЃўрЃЎрЃџрЃў while, рЃАрЃљрЃюрЃљрЃЏ nextStep false рЃљрЃа рЃњрЃљрЃ«рЃЊрЃћрЃЉрЃљ*/

while (nextStep) {
  /* рЃЋрЃЌрЃ«рЃЮрЃЋрЃЮрЃЌ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃА рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃЮрЃА рЃфрЃўрЃцрЃарЃў рЃЏрЃЮрЃфрЃћрЃЏрЃБрЃџ рЃЊрЃўрЃљрЃърЃљрЃќрЃЮрЃюрЃерЃў*/

  let user = prompt(`рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћрЃЌ рЃЎрЃЮрЃарЃћрЃЦрЃбрЃБрЃџрЃў рЃЏрЃЮрЃюрЃљрЃфрЃћрЃЏрЃў ${minNumber} - ${maxNumber}`);
  /* рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃА рЃЏрЃўрЃћрЃа рЃерЃћрЃДрЃЋрЃљрЃюрЃўрЃџрЃў рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЉрЃљ рЃарЃўрЃфрЃ«рЃЋрЃљрЃЌ рЃњрЃљрЃЊрЃљрЃЋрЃљрЃЦрЃфрЃўрЃЮрЃЌ + = Number(), рЃарЃљрЃЊрЃњрЃљрЃю prompt String рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА*/
  user = +user;
  /*рЃерЃћрЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЌ, рЃљрЃарЃўрЃА рЃЌрЃБ рЃљрЃарЃљ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃА рЃЏрЃўрЃћрЃа рЃерЃћрЃДрЃЋрЃљрЃюрЃўрЃџрЃў рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЉрЃљ рЃарЃўрЃфрЃ«рЃЋрЃў рЃЊрЃљ рЃљрЃарЃўрЃА рЃЌрЃБ рЃљрЃарЃљ рЃЏрЃЮрЃфрЃћрЃЏрЃБрЃџ рЃЊрЃўрЃљрЃърЃљрЃќрЃЮрЃюрЃерЃў*/
  if (isNaN(user) || user < minNumber || user > maxNumber) {
    alert("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћрЃЌ рЃарЃўрЃфрЃ«рЃЋрЃў");
  } else {
    /* рЃЌрЃБ рЃфрЃўрЃцрЃарЃўрЃљ, рЃЏрЃљрЃњрЃарЃљрЃЏ рЃЊрЃљрЃњрЃћрЃюрЃћрЃарЃўрЃарЃћрЃЉрЃБрЃџрЃўрЃАрЃњрЃљрЃю рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃБрЃџрЃў, рЃЊрЃљрЃЋрЃљрЃЌрЃЋрЃџрЃћрЃЋрЃўрЃюрЃЮрЃЌ рЃЏрЃфрЃЊрЃћрЃЉрЃЮрЃџрЃўрЃА рЃарЃљрЃЮрЃЊрЃћрЃюрЃЮрЃЉрЃљ*/
    attempts++;
    /* рЃБрЃЎрЃБрЃЎрЃљрЃЋрЃерЃўрЃарЃў рЃўрЃЏрЃўрЃА рЃЏрЃўрЃ«рЃћрЃЊрЃЋрЃўрЃЌ рЃарЃљрЃА рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃА рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃў*/
    if (user < answer) {
      alert("рЃЊрЃљрЃЉрЃљрЃџрЃўрЃљ ­Ъўћ рЃАрЃфрЃљрЃЊрЃћрЃЌ рЃБрЃцрЃарЃЮ рЃЏрЃљрЃдрЃљрЃџрЃў");
    } else if (user > answer) {
      alert("рЃЏрЃљрЃдрЃљрЃџрЃўрЃљ­Ъўћ рЃфрЃЊрЃљрЃЊрЃћрЃЌ рЃЊрЃљрЃЉрЃљрЃџрЃў");
    } else {
      /*рЃЌрЃБ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃА рЃЏрЃўрЃћрЃа рЃерЃћрЃДрЃЋрЃљрЃюрЃўрЃџрЃў рЃфрЃўрЃцрЃарЃў рЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃўрЃЌ рЃарЃўрЃфрЃ«рЃЋрЃА, рЃЊрЃљрЃљрЃАрЃарЃБрЃџрЃћрЃЌ рЃфрЃўрЃЎрЃџрЃў рЃЊрЃљ рЃљрЃЕрЃЋрЃћрЃюрЃћрЃЌ рЃгрЃљрЃарЃЏрЃљрЃбрЃћрЃЉрЃўрЃА рЃерЃћрЃбрЃДрЃЮрЃЉрЃўрЃюрЃћрЃЉрЃљ */
      alert(`рЃњрЃўрЃџрЃЮрЃфрЃљрЃЌ ${answer}, ­Ъўђ ${attempts} рЃЏрЃфрЃЊрЃћрЃџрЃЮрЃЉрЃљрЃерЃў`);
      nextStep = false;
    }
  }
}
/*рЃерЃћрЃЋрЃЦрЃЏрЃюрЃљрЃЌ const calc рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃљрЃарЃўрЃА  DOM рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃўрЃА рЃюрЃљрЃгрЃўрЃџрЃў рЃЎрЃџрЃљрЃАрЃўрЃЌ  "calculator",*/
const calc = document.querySelector(".calculator");
/*рЃерЃћрЃЋрЃЦрЃЏрЃюрЃљрЃЌ const result рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃљрЃарЃўрЃА  DOM рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃўрЃА рЃюрЃљрЃгрЃўрЃџрЃў  id "result",*/
const result = document.querySelector("#result");
/*рЃЋрЃљрЃДрЃћрЃюрЃћрЃЉрЃЌ addEventListener class = "calculator", рЃарЃЮрЃЊрЃћрЃАрЃљрЃф рЃљрЃЏ рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃўрЃА рЃерЃўрЃњрЃюрЃўрЃЌ рЃ«рЃЊрЃћрЃЉрЃљ рЃЊрЃљрЃгрЃЎрЃљрЃърЃБрЃюрЃћрЃЉрЃљ, рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ рЃерЃћрЃАрЃарЃБрЃџрЃћрЃЉрЃБрЃџрЃўрЃљ. */
calc.addEventListener("click", function (event) {
  /*рЃЋрЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃЌ рЃљрЃарЃўрЃА рЃЌрЃБ рЃљрЃарЃљ рЃАрЃљрЃЏрЃўрЃќрЃюрЃћ рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃў (рЃарЃљрЃќрЃћрЃф рЃЊрЃљрЃљрЃгрЃЎрЃљрЃърЃБрЃюрЃћрЃЌ)  рЃдрЃўрЃџрЃљрЃЎрЃў, рЃЌрЃБ рЃљрЃарЃљ, рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ рЃгрЃДрЃЊрЃћрЃЉрЃљ */
  if (!event.target.classList.contains("btn")) return;
  /* рЃўрЃЏ рЃдрЃўрЃџрЃљрЃЎрЃўрЃА рЃбрЃћрЃЦрЃАрЃбрЃБрЃарЃў рЃерЃўрЃюрЃљрЃљрЃарЃАрЃўрЃА рЃЏрЃўрЃдрЃћрЃЉрЃљ, рЃарЃЮрЃЏрЃџрЃўрЃџрЃќрЃћрЃф рЃЊрЃљрЃЋрЃљрЃгрЃЎрЃљрЃърЃБрЃюрЃћрЃЌ*/
  const value = event.target.innerText;
  switch (value) {
    case "C":
      /*рЃЌрЃБ рЃдрЃўрЃџрЃљрЃЎрЃў "C" рЃЊрЃљрЃЋрЃљрЃгрЃЎрЃљрЃърЃБрЃюрЃћрЃЌ, рЃерЃџрЃўрЃА рЃарЃћрЃќрЃБрЃџрЃбрЃљрЃбрЃА  */
      result.innerText = "";
      break;
    case "Рєљ":
      /*рЃЌрЃБ рЃдрЃўрЃџрЃљрЃЎрЃў "Рєљ"рЃЊрЃљрЃЋрЃљрЃгрЃЎрЃљрЃърЃБрЃюрЃћрЃЌ, рЃерЃџрЃўрЃА рЃЉрЃЮрЃџрЃЮ рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮрЃА  */
      result.innerText = result.innerText.slice(0, -1);
      break;
    /* рЃЌрЃБ рЃЊрЃљрЃЋрЃљрЃгрЃЎрЃљрЃърЃБрЃюрЃћрЃЉрЃЌ "=" рЃњрЃљрЃЏрЃЮрЃљрЃЋрЃА рЃњрЃљрЃљрЃюрЃњрЃљрЃарЃўрЃерЃћрЃЉрЃўрЃА рЃерЃћрЃЊрЃћрЃњрЃў */
    case "=":
      /*рЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃА рЃљрЃарЃўрЃА рЃЌрЃБ рЃљрЃарЃљ рЃћрЃџрЃћрЃЏрЃћрЃюрЃб  id "result" рЃерЃўрЃњрЃюрЃўрЃЌ рЃњрЃљрЃарЃЊрЃљ рЃЦрЃЋрЃћрЃЏрЃЮрЃЌ рЃЕрЃљрЃЏрЃЮрЃЌрЃЋрЃџрЃўрЃџрЃў рЃАрЃЮрЃЏрЃЉрЃЮрЃџрЃЮрЃћрЃЉрЃўрЃАрЃљ*/
      if (result.innerText.search(/[^0-9*/+-.()]/im) != -1) return;
      /*рЃЌрЃБ рЃљрЃарЃљрЃљ рЃЏрЃљрЃЌрЃћрЃЏрЃљрЃбрЃўрЃЎрЃБрЃарЃў рЃџрЃЮрЃњрЃўрЃЎрЃБрЃарЃў рЃљрЃЏрЃЮрЃфрЃљрЃюрЃљ рЃЏрЃўрЃЌрЃўрЃЌрЃћрЃЉрЃБрЃџрЃў рЃљрЃњрЃЊрЃћрЃЉрЃА "EROOR!",рЃЌрЃБ рЃљрЃарЃљ рЃЊрЃљ рЃњрЃљрЃЏрЃЮрЃљрЃюрЃњрЃљрЃарЃўрЃерЃћрЃЉрЃА рЃЏрЃљрЃбрЃћрЃЏрЃљрЃбрЃўрЃЎрЃБрЃа рЃљрЃЏрЃЮрЃфрЃљрЃюрЃљрЃА */
      try {
        result.innerText = eval(result.innerText).toFixed(2);
      } catch {
        result.innerText = "EROOR!";
      }
      break;
    default:
      /*рЃЌрЃБ рЃарЃЮрЃЏрЃћрЃџрЃўрЃЏрЃћ рЃАрЃ«рЃЋрЃљ рЃдрЃўрЃџрЃљрЃЎрЃА рЃЊрЃљрЃљрЃгрЃЎрЃљрЃърЃБрЃюрЃћрЃЉрЃЌ, рЃЏрЃўрЃАрЃў рЃбрЃћрЃЦрЃАрЃбрЃў рЃћрЃЏрЃљрЃбрЃћрЃЉрЃљ рЃЏрЃўрЃЏрЃЊрЃўрЃюрЃљрЃарЃћ рЃерЃћрЃЊрЃћрЃњрЃўрЃА */
      result.innerText += value;
  }
});
